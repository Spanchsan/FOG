package com.mygdx.game.gameUtils;

public class User {
    private String name;
    private String key;
    private int pointsG, pointsO, pointsF;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public int getPointsG() {
        return pointsG;
    }

    public void setPointsG(int pointsG) {
        this.pointsG = pointsG;
    }

    public int getPointsO() {
        return pointsO;
    }

    public void setPointsO(int pointsO) {
        this.pointsO = pointsO;
    }

    public int getPointsF() {
        return pointsF;
    }

    public void setPointsF(int pointsF) {
        this.pointsF = pointsF;
    }
}
